
public class Square {

	private int x;
	private String s;
	
	public Square(int x, String s) {
		this.x = x;
		this.s = s;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public String getS() {
		return s;
	}
	public void setS(String s) {
		this.s = s;
	}
}
